// Generate src/render/unicode-font-routing.noto-linux.generated.ts — the
// per-Unicode-block fallback routing for the DESKTOP-LINUX **Noto profile**
// (DM-1404), the analogue of tools/probe-983-genroutes-linux.mjs for the bare
// Playwright image (DM-984).
//
// Source data (both produced INSIDE the Playwright *-noble container by
// tools/calibrate-linux-noto-profile.sh, which strips the noble-image-only
// fallback fonts — WenQuanYi / FreeFont / IPAGothic / Loma / Unifont — and
// installs the mainstream desktop Noto set so fontconfig prioritizes Noto, then
// runs the same CDP sweep + an fc-match family->file resolution):
//
//   tests/output/unicode-fonts.noto-linux.json   block -> [Chromium-painted families]
//   tests/output/family-to-path.noto-linux.json  family -> { file, postscriptName }
//
// Unlike the bare generator (a tiny hand-maintained FAMILY_TO_PATH), the Noto
// profile routes ~150 script-specific Noto families, so we key the generated
// table by the family->file map the container produced — no hand maintenance.
//
// Keys are namespaced `un-...` (Noto) so they can't collide with the bare
// `u-...` keys or the hand-coded LINUX_FONT_PATHS keys. Run on the macOS host
// (the Linux paths are static string literals).

import { readFileSync, writeFileSync } from "node:fs";

const sweep = JSON.parse(readFileSync("tests/output/unicode-fonts.noto-linux.json", "utf-8"));
const fam2path = JSON.parse(readFileSync("tests/output/family-to-path.noto-linux.json", "utf-8"));

const familyToKey = new Map();
function makeKey(family) {
  if (familyToKey.has(family)) return familyToKey.get(family);
  const slug = "un-" + family.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
  familyToKey.set(family, slug);
  return slug;
}

const ranges = [];
const unresolved = new Set();
for (const [blockName, families] of Object.entries(sweep.blockToFamilies)) {
  const m = /^([0-9A-Fa-f]+)-([0-9A-Fa-f]+)/.exec(blockName);
  if (m == null) continue;
  const start = parseInt(m[1], 16);
  const end = parseInt(m[2], 16);
  const primary = families && families[0];
  if (primary == null) continue;
  const resolved = fam2path[primary];
  if (resolved == null || !resolved.file) { unresolved.add(primary); continue; } // emoji (null) / unresolved
  ranges.push({ start, end, family: primary, blockName, fontKey: makeKey(primary), resolved });
}
ranges.sort((a, b) => a.start - b.start);

// Dedup ranges by [start,end] — the per-block fixtures are SHARDED (e.g. the
// huge CJK-Ext-B block appears as ...b.0, ...b.1, … each carrying the same
// codepoint range), so the raw list repeats identical ranges. When shards of the
// same range disagree on the font (a shard whose first sampled cells were
// unassigned can resolve to generic Noto Sans instead of the script face), take
// the MAJORITY family across shards rather than the first — so the dominant face
// wins. (The DM-1416 live resolver still covers any per-codepoint miss.)
const byRange = new Map(); // "start-end" -> { rep, votes: Map<key,count> }
for (const r of ranges) {
  const rk = `${r.start}-${r.end}`;
  let e = byRange.get(rk);
  if (e == null) { e = { rep: r, votes: new Map() }; byRange.set(rk, e); }
  e.votes.set(r.fontKey, (e.votes.get(r.fontKey) ?? 0) + 1);
}
const finalRanges = [];
for (const { rep, votes } of byRange.values()) {
  let bestKey = rep.fontKey, best = -1;
  for (const [k, n] of votes) if (n > best) { best = n; bestKey = k; }
  if (votes.size > 1) console.log(`  range ${rep.start.toString(16)}-${rep.end.toString(16)}: ${[...votes.entries()].map(([k, n]) => `${k}×${n}`).join(", ")} -> ${bestKey}`);
  finalRanges.push({ ...rep, fontKey: bestKey, resolved: ranges.find(x => x.fontKey === bestKey)?.resolved ?? rep.resolved });
}
finalRanges.sort((a, b) => a.start - b.start);

const pathByKey = new Map();
for (const r of finalRanges) if (!pathByKey.has(r.fontKey)) pathByKey.set(r.fontKey, r.resolved);

let out = "// AUTO-GENERATED by tools/probe-983-genroutes-noto-linux.mjs — do not edit by hand.\n";
out += "// Desktop-Linux **Noto profile** per-Unicode-block fallback routing (DM-1404).\n";
out += "// Source data: tests/output/unicode-fonts.noto-linux.json + family-to-path.noto-linux.json,\n";
out += "// produced by tools/calibrate-linux-noto-profile.sh inside the Playwright *-noble\n";
out += "// container with the noble-only fallback fonts stripped and the mainstream Noto set\n";
out += "// installed, so Chromium's fontconfig picks match a real desktop-Linux host.\n";
out += "// Selected at runtime by `linuxFontProfile() === 'noto'`; keys namespaced `un-...`.\n";
out += "\n";
out += "export interface UnicodeFontEntry { path: string; postscriptName?: string; extractor?: \"fontkit\" | \"native\" }\n\n";
out += "export const UNICODE_FONT_PATHS_NOTO_LINUX: Record<string, UnicodeFontEntry> = {\n";
for (const [key, resolved] of [...pathByKey.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
  const psn = resolved.postscriptName ? `, postscriptName: ${JSON.stringify(resolved.postscriptName)}` : "";
  out += `  ${JSON.stringify(key)}: { path: ${JSON.stringify(resolved.file)}${psn} },\n`;
}
out += "};\n\n";
out += "/** [start, end, fontKey] tuples sorted by start. Binary-searched per codepoint. */\n";
out += "export const UNICODE_FONT_RANGES_NOTO_LINUX: ReadonlyArray<readonly [number, number, string]> = [\n";
for (const r of finalRanges) {
  out += `  [0x${r.start.toString(16).toUpperCase()}, 0x${r.end.toString(16).toUpperCase()}, ${JSON.stringify(r.fontKey)}], // ${r.blockName} (${r.family})\n`;
}
out += "];\n";

writeFileSync("src/render/unicode-font-routing.noto-linux.generated.ts", out);
console.log(`Generated ${finalRanges.length} Noto ranges covering ${pathByKey.size} fonts.`);
if (unresolved.size > 0) {
  console.log("Skipped (null=emoji raster, or unresolved family):");
  for (const f of unresolved) console.log("  - " + JSON.stringify(f));
}
