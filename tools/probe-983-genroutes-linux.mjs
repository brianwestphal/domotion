// Generate src/render/unicode-font-routing.linux.generated.ts from the
// /tmp/unicode-fonts.linux.json sweep produced by `tools/probe-983-sweep.mjs`
// inside the same `mcr.microsoft.com/playwright:v<version>-noble` Docker
// container CI uses. For each unicode block (filename-derived codepoint
// range), pick the font family Chrome chose first on Linux, then resolve
// that family to an on-disk path inside the container.
//
// Container-specific resolution: family names map to absolute paths under
// `/usr/share/fonts/...`. The bare Playwright image ships a minimal set —
// Liberation, FreeFont, WenQuanYi Zen Hei, IPAGothic, Loma, plus Unifont
// for the "last resort" pixel-art coverage Chrome falls through to when
// no covering font has the glyph. ~10 distinct fonts cover all 330 blocks.
//
// Run from the repo root on macOS (Linux paths are STATIC strings in the
// generated TS file — they don't need to resolve on the macOS host):
//
//   tools/probe-983-sweep.mjs is run INSIDE the Linux container via
//   scripts/test-linux-docker.sh; it writes tests/output/unicode-fonts.linux.json
//   on the mounted repo, which this generator (run on the macOS host)
//   reads to produce src/render/unicode-font-routing.linux.generated.ts.
//
// Per-codepoint crash-detection (fontkit GSUB bugs that crash on macOS
// Sangam MN / Indic Noto) is NOT run here — Linux fonts in the bare image
// don't include the crashers, and the platform's FreeType helper covers
// any Linux fontkit failures via the `extractor: "native"` mechanism.

import { readFileSync, writeFileSync } from "node:fs";

// Linux paths from the Playwright Docker image. Verified via `fc-list` /
// `fc-match -f '%{file}\n' '<family>'` inside the container.
const LIB      = "/usr/share/fonts/truetype/liberation";
const FREEFONT = "/usr/share/fonts/truetype/freefont";
const WQY      = "/usr/share/fonts/truetype/wqy/wqy-zenhei.ttc";
const UNIFONT  = "/usr/share/fonts/opentype/unifont";

const FAMILY_TO_PATH = {
  // Chrome's CDP `CSS.getPlatformFontsForNode` reports these family names
  // for the bare Playwright image on Linux. Values are absolute paths
  // (string literals — they only need to resolve at runtime inside the
  // container or on a similarly-configured Linux host, not on macOS).
  "Unifont":           { path: `${UNIFONT}/unifont.otf` },
  "Unifont Upper":     { path: `${UNIFONT}/unifont_upper.otf` },
  "Liberation Sans":   { path: `${LIB}/LiberationSans-Regular.ttf` },
  "Liberation Serif":  { path: `${LIB}/LiberationSerif-Regular.ttf` },
  "FreeSans":          { path: `${FREEFONT}/FreeSans.ttf` },
  "FreeSerif":         { path: `${FREEFONT}/FreeSerif.ttf` },
  "FreeMono":          { path: `${FREEFONT}/FreeMono.ttf` },
  "WenQuanYi Zen Hei": { path: WQY, postscriptName: "WenQuanYiZenHei" },
  "IPAGothic":         { path: "/usr/share/fonts/truetype/fonts-japanese-gothic.ttf" },
  "Loma":              { path: "/usr/share/fonts/opentype/tlwg/Loma.otf" },
  // Apple Color Emoji's Linux counterpart — handled via the raster path,
  // not the glyph-path keys; skip routing.
  "Noto Color Emoji":  null,
};

function resolveFamily(family) {
  if (family in FAMILY_TO_PATH) return FAMILY_TO_PATH[family];
  return null;
}

const data = JSON.parse(readFileSync("tests/output/unicode-fonts.linux.json", "utf-8"));
const blockToFamilies = data.blockToFamilies;

const ranges = []; // { startHex, endHex, family, blockName, fontKey }
const familyToKey = new Map();
const unresolved = new Set();

function makeKey(family) {
  if (familyToKey.has(family)) return familyToKey.get(family);
  // Lowercase, dash-separated, prefixed with "u-" to avoid clashing with
  // hand-coded keys in LINUX_FONT_PATHS.
  const slug = "u-" + family.toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
  familyToKey.set(family, slug);
  return slug;
}

for (const [blockName, families] of Object.entries(blockToFamilies)) {
  const m = /^([0-9A-F]+)-([0-9A-F]+)/.exec(blockName);
  if (m == null) continue;
  const start = parseInt(m[1], 16);
  const end = parseInt(m[2], 16);
  const primary = families[0];
  if (primary == null) continue;
  const resolved = resolveFamily(primary);
  if (resolved == null) {
    unresolved.add(primary);
    continue;
  }
  ranges.push({ start, end, family: primary, blockName, fontKey: makeKey(primary), resolved });
}

ranges.sort((a, b) => a.start - b.start);

let out = "// AUTO-GENERATED by tools/probe-983-genroutes-linux.mjs — do not edit by hand.\n";
out += "// Source data: tests/output/unicode-fonts.linux.json (produced by\n";
out += "// tools/probe-983-sweep.mjs inside the Playwright Docker container — see\n";
out += "// scripts/test-linux-docker.sh and DM-984).\n";
out += "// Maps Unicode blocks to the Linux font family Chrome's fontconfig\n";
out += "// fallback picks via CDP `CSS.getPlatformFontsForNode`. Consulted by\n";
out += "// `linuxFallbackChain` as the final fallback when no hand-coded route\n";
out += "// matches a given codepoint. Paths target the bare Playwright image\n";
out += "// — Unifont / Unifont Upper carry the bulk of non-Latin coverage as\n";
out += "// pixel-art \"last resort\" glyphs (matching what Chrome paints here).\n";
out += "\n";
out += "export interface UnicodeFontEntry { path: string; postscriptName?: string; extractor?: \"fontkit\" | \"native\" }\n\n";
out += "export const UNICODE_FONT_PATHS_LINUX: Record<string, UnicodeFontEntry> = {\n";
const seenKeys = new Set();
for (const r of ranges) {
  if (seenKeys.has(r.fontKey)) continue;
  seenKeys.add(r.fontKey);
  const psn = r.resolved.postscriptName != null ? `, postscriptName: ${JSON.stringify(r.resolved.postscriptName)}` : "";
  out += `  ${JSON.stringify(r.fontKey)}: { path: ${JSON.stringify(r.resolved.path)}${psn} },\n`;
}
out += "};\n\n";
out += "/** [start, end, fontKey] tuples sorted by start. Probed by binary-searching for the matching range. */\n";
out += "export const UNICODE_FONT_RANGES_LINUX: ReadonlyArray<readonly [number, number, string]> = [\n";
for (const r of ranges) {
  out += `  [0x${r.start.toString(16).toUpperCase()}, 0x${r.end.toString(16).toUpperCase()}, ${JSON.stringify(r.fontKey)}], // ${r.blockName} (${r.family})\n`;
}
out += "];\n";

writeFileSync("src/render/unicode-font-routing.linux.generated.ts", out);

console.log(`Generated ${ranges.length} ranges covering ${seenKeys.size} fonts.`);
if (unresolved.size > 0) {
  console.log(`Unresolved font families (skipped — no FAMILY_TO_PATH entry; add a path to the generator if you want to route these):`);
  for (const f of unresolved) console.log(`  - ${JSON.stringify(f)}`);
}
