#!/usr/bin/env node
// Pre-build: bundle `src/scrubber/client.ts` into a single IIFE the
// `svg-scrubber` HTTP server serves as `/client.js`. Output is written
// to `src/scrubber/client.bundle.generated.ts` as an exported string so
// `tsc -p tsconfig.build.json` picks it up and ships it inside `dist/`. Mirrors
// scripts/build-review-client.mjs (DM-1040).

import { build } from "esbuild";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const ENTRY = resolve(ROOT, "src/scrubber/client.tsx");
const OUT = resolve(ROOT, "src/scrubber/client.bundle.generated.ts");

const result = await build({
  entryPoints: [ENTRY],
  bundle: true,
  write: false,
  format: "iife",
  target: "es2022",
  platform: "browser",
  jsx: "automatic",
  jsxImportSource: "kerfjs",
  legalComments: "none",
  minify: false,
  treeShaking: true,
});

const code = result.outputFiles[0].text;
const banner = [
  "// AUTO-GENERATED by scripts/build-scrubber-client.mjs — do NOT edit by hand.",
  "// The source lives at src/scrubber/client.tsx; rerun the build to regenerate.",
  "",
  `export const SCRUBBER_CLIENT_JS: string = ${JSON.stringify(code)};`,
  "",
].join("\n");
writeFileSync(OUT, banner);

const KB = (code.length / 1024).toFixed(1);
process.stdout.write(`[build-scrubber-client] wrote ${OUT} (${KB} KB bundle)\n`);
