#!/usr/bin/env node
// Pre-build: bundle `src/review/client.tsx` into a single IIFE the
// `svg-review` HTTP server can serve as `/client.js`. Output is written
// to `src/review/client.bundle.generated.ts` as an exported string so
// `tsc -p tsconfig.build.json` picks it up and ships it inside `dist/`.
//
// Why a pre-build: the server.ts module loads the bundle string at
// runtime; if we instead invoked esbuild at server startup (the original
// draft), the consumer would need `esbuild` + the original `.tsx` shipped
// in the package — both too heavy for a published npm bundle. Baking the
// bundle into a `.generated.ts` keeps the runtime dep surface to what
// the rest of the package already needs (kerfjs jsx is compiled out by
// esbuild at this step).

import { build } from "esbuild";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const ENTRY = resolve(ROOT, "src/review/client.tsx");
const OUT = resolve(ROOT, "src/review/client.bundle.generated.ts");

const result = await build({
  entryPoints: [ENTRY],
  bundle: true,
  write: false,
  format: "iife",
  target: "es2022",
  platform: "browser",
  jsx: "automatic",
  jsxImportSource: "kerfjs",
  legalComments: "none",
  minify: false,
  treeShaking: true,
});

const code = result.outputFiles[0].text;
const banner = [
  "// AUTO-GENERATED by scripts/build-review-client.mjs — do NOT edit by hand.",
  "// The source lives at src/review/client.tsx; rerun the build to regenerate.",
  "",
  `export const REVIEW_CLIENT_JS: string = ${JSON.stringify(code)};`,
  "",
].join("\n");
writeFileSync(OUT, banner);

const KB = (code.length / 1024).toFixed(1);
process.stdout.write(`[build-review-client] wrote ${OUT} (${KB} KB bundle)\n`);
